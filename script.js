let currentPage = 0;

function loadPage(pageIndex) {
    const contentDiv = document.getElementById('content');
    const page = pages[pageIndex];
    contentDiv.innerHTML = `
        <h1>${page.title}</h1>
        ${page.image ? `<img src="${page.image}" alt="${page.title}" style="max-width: 100%; margin: 20px 0;">` : ""}
        <div id="info-frame">
            <p>${page.description}</p>
        </div>
    `;
    document.getElementById('prevButton').disabled = pageIndex === 0;
    document.getElementById('nextButton').disabled = pageIndex === pages.length - 1;
}

function nextPage() {
    if (currentPage < pages.length - 1) {
        currentPage++;
        loadPage(currentPage);
    }
}

function prevPage() {
    if (currentPage > 0) {
        currentPage--;
        loadPage(currentPage);
    }
}

window.onload = () => {
    loadPage(currentPage);
};
